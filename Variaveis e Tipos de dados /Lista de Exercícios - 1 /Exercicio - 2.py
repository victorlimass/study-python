primeiraNota = int(input("Digite a primeira nota: "))
segundaNota =  int(input("Digite a segunda nota: "))
terceiraNota = int(input("Digite a terceira nota: "))
quartaNota = int(input("Digite a quarta nota: "))

media_aritmetica = (primeiraNota + segundaNota + terceiraNota + quartaNota) / 4

print(f"A média aritmética é {media_aritmetica}")
